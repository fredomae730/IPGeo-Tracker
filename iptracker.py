#!/usr/bin/env python3
import urllib.request
import urllib.error
import json
import socket
import sys
import os
import re
from datetime import datetime

# Colors for Termux
class Colors:
    HEADER = '\033[95m'
    BLUE = '\033[94m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    RED = '\033[91m'
    WHITE = '\033[97m'
    RESET = '\033[0m'
    BOLD = '\033[1m'

def clear_screen():
    os.system('clear')

def clean_input(text):
    text = text.strip()
    text = re.sub(r'[:/]+$', '', text)
    return text.strip()

def get_public_ip():
    try:
        with urllib.request.urlopen('https://api.ipify.org?format=json', timeout=5) as response:
            data = json.loads(response.read().decode())
            return data['ip']
    except:
        return None

def track_ip(ip_address):
    ip_address = clean_input(ip_address)
    
    print(f"\n{Colors.YELLOW}[*] Tracking IP: {Colors.CYAN}{ip_address}{Colors.RESET}")
    print(f"{Colors.YELLOW}[*] Time: {Colors.GREEN}{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}{Colors.RESET}\n")
    
    try:
        url = f'http://ip-api.com/json/{ip_address}?fields=status,country,countryCode,region,regionName,city,zip,lat,lon,timezone,isp,org,as,query'
        req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})
        
        with urllib.request.urlopen(req, timeout=10) as response:
            data = json.loads(response.read().decode())
            
            if data.get('status') == 'success':
                print(f"{Colors.GREEN}{Colors.BOLD}")
                print("??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????")
                print("???           IP INFORMATION              ???")
                print("??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????")
                print(f"{Colors.RESET}")
                
                info = [
                    ('IP Address', data.get('query', 'N/A')),
                    ('Country', data.get('country', 'N/A')),
                    ('Country Code', data.get('countryCode', 'N/A')),
                    ('Region', data.get('regionName', 'N/A')),
                    ('City', data.get('city', 'N/A')),
                    ('ZIP Code', data.get('zip', 'N/A')),
                    ('Latitude', data.get('lat', 'N/A')),
                    ('Longitude', data.get('lon', 'N/A')),
                    ('Timezone', data.get('timezone', 'N/A')),
                    ('ISP', data.get('isp', 'N/A')),
                    ('Organization', data.get('org', 'N/A')),
                    ('AS Number', data.get('as', 'N/A'))
                ]
                
                for label, value in info:
                    if value and value != 'N/A':
                        print(f"{Colors.CYAN}{label:<15}{Colors.RESET}: {Colors.WHITE}{value}{Colors.RESET}")
                
                if data.get('lat') and data.get('lon'):
                    print(f"\n{Colors.GREEN}[???] Google Maps: https://www.google.com/maps?q={data['lat']},{data['lon']}{Colors.RESET}")
                
                return True
            else:
                print(f"{Colors.RED}Error: {data.get('message', 'Unknown')}{Colors.RESET}")
                return False
                
    except Exception as e:
        print(f"{Colors.RED}Error: {e}{Colors.RESET}")
        return False

def main():
    clear_screen()
    print(f"{Colors.CYAN}{Colors.BOLD}")
    print("??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????")
    print("???        IP TRACKER PRO v3.0             ???")
    print("???      Advanced IP Intelligence          ???")
    print("??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????")
    print(f"{Colors.RESET}")
    
    while True:
        print(f"\n{Colors.YELLOW}??????[{Colors.CYAN}IP Tracker{Colors.YELLOW}]")
        print(f"???????????? {Colors.GREEN}$ {Colors.RESET}", end='')
        
        target = input().strip()
        
        if target.lower() in ['exit', 'quit', 'q']:
            print(f"{Colors.RED}[!] Exiting...{Colors.RESET}")
            sys.exit(0)
        
        if target.lower() == 'myip':
            my_ip = get_public_ip()
            if my_ip:
                print(f"{Colors.GREEN}[???] Your public IP: {my_ip}{Colors.RESET}")
                track_ip(my_ip)
            else:
                print(f"{Colors.RED}[???] Could not detect your IP{Colors.RESET}")
        elif target:
            track_ip(target)
        else:
            print(f"{Colors.RED}[!] Please enter an IP address or 'myip'{Colors.RESET}")
        
        print(f"\n{Colors.YELLOW}[*] Press Enter to continue{Colors.RESET}")
        input()

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print(f"\n{Colors.RED}[!] Interrupted{Colors.RESET}")
        sys.exit(0)
